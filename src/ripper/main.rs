#[allow(dead_code)]
enum ToolMode {
    Palette,
    Tile,
    Sprite,
    Map
}

#[derive(Copy, Clone, Hash, Eq, PartialEq)]
struct PaletteEntry {
    pub r: u8,
    pub g: u8,
    pub b: u8,
    pub a: u8
}

impl PaletteEntry {
    pub fn new() -> PaletteEntry {
        PaletteEntry { r: 0, g: 0, b: 0, a: 0xff }
    }
}

#[derive(Copy, Clone, Hash, Eq, PartialEq)]
struct TileMapEntry(u16, u8, u8);

#[derive(Copy, Clone, Hash, Eq, PartialEq)]
struct Palette {
    entries: [PaletteEntry; 4]
}

fn find_palette_match(
        color_tile: &[Color;8*8],
        index_tile: &[u8;8*8]) -> u8 {
    let mut palette = [Color::RGBA(0, 0, 0, 0xff);4];

    for _ty in 0..8 as usize {
        for _tx in 0..8 as usize {
            let color_pixel = color_tile[_ty * 8 + _tx];
            let index_pixel = index_tile[_ty * 8 + _tx];
            let pal_color = palette[index_pixel as usize];
            if color_pixel != pal_color {
                palette[index_pixel as usize] = color_pixel;
            }
        }
    }

    println!("---------------");
    for color in palette.iter() {
        println!("r: {}, g: {}, b: {}, a: {}", color.r, color.g, color.b, color.a);
    }
    println!();

    let mut scores:Vec<u8> = vec![];
    for pal in PAL_CNTL.iter() {
        let mut score = 0;
        for index in 0..4 {
            let palette_color = palette[index as usize];
            let entry = &pal.entries[index as usize];
            let entry_color = Color::RGBA(entry.r, entry.g, entry.b, entry.a);
            if palette_color == entry_color {
                if palette_color.r == 0 && palette_color.g == 0 && palette_color.b == 0 {
                    if index == 0 || index == 3 {
                        score += 1;
                    }
                } else {
                    score += 1;
                }
            }
        }
        scores.push(score);
    }

    let mut highest_score = 0;
    let mut palette_number = 0;
    for i in 0..scores.len() {
        let score = scores[i];
        if score > highest_score {
            println!("palette {} has a score of {}", i, score);
            highest_score = score;
            palette_number = i;
        }
    }

    println!("palette_number = {}", palette_number);
    println!();
    println!("---------------");
    palette_number as u8
}

fn create_match_bitmap(
        canvas: &mut WindowCanvas,
        cx:usize,
        cy:usize) -> [Color; 8*8] {
    let mut tile_data = [Color::RGBA(0, 0, 0, 0xff); 8*8];

    for _ty in 0..8 as usize {
        for _tx in 0..8 as usize {
            let pixels = canvas
                .read_pixels(
                    Rect::new((cx + _tx) as i32, (cy + _ty) as i32, 1, 1),
                    canvas.default_pixel_format())
                .unwrap();
            tile_data[_ty * 8 + _tx] = Color::RGBA(pixels[0], pixels[1], pixels[2], 0xff);
        }
    }

    tile_data
}

fn run(mode:ToolMode, png: &Path) {
    let sdl_context = sdl2::init().unwrap();
    let video_subsystem = sdl_context.video().unwrap();

    struct WindowSize(u32, u32);
    let window_size = match mode {
        ToolMode::Palette   => WindowSize(800,  600),
        ToolMode::Tile      => WindowSize(1151, 17),
        ToolMode::Sprite    => WindowSize(1155, 33),
        ToolMode::Map       => WindowSize(256, 256)
    };

    let _image_context = sdl2::image::init(INIT_PNG).unwrap();
    let window = video_subsystem.window("Rusty Kong Ripper Tool", window_size.0, window_size.1)
        .position_centered()
        .build()
        .unwrap();

    let mut canvas = window.into_canvas().software().build().unwrap();
    let texture_creator = canvas.texture_creator();
    let texture = texture_creator.load_texture(png).unwrap();
    let texture_info = texture.query();

    canvas
        .copy(
            &texture,
            None,
            Rect::new(0, 0, texture_info.width, texture_info.height))
        .expect("Render failed");
    canvas.present();

    match mode {
        ToolMode::Map => {
            let mut tile_map = [TileMapEntry(0,0,0); 32*32];
            let mut _cy:usize = 0;
            let mut _cx:usize = 0;
            for _y in 0..32 {
                for _x in 0..32 {
                    let tile_data = create_match_bitmap(&mut canvas, _cx, _cy);
                    for tile_number in 0..256 {
                        let ref_tile:&[u8; 64] = &TILE_BITMAPS[tile_number];
                        let mut equal = true;
                        for pixel in 0..64 {
                            let ref_has_pixel = ref_tile[pixel] != 0;
                            let map_has_pixel = tile_data[pixel].r != 0
                                || tile_data[pixel].g != 0
                                || tile_data[pixel].b != 0;
                            if ref_has_pixel != map_has_pixel {
                                equal = false;
                                break;
                            }
                        }
                        if equal {
                            let palette_number = find_palette_match(&tile_data, ref_tile);
                            tile_map[_y * 32 + _x] = TileMapEntry(
                                tile_number as u16,
                                palette_number as u8,
                                0);
                            break;
                        }
                    }
                    _cx += 8;
                }
                _cx = 0;
                _cy += 8;
            }


            println!("static TILE_MAP:[TileMapEntry; 32*32] = [");
            for _y in 0..32 as usize {
                print!("\t");
                for _x in 0..32 as usize {
                    if _x > 0 {
                        print!(",");
                    }
                    let entry = tile_map[_y * 32 + _x];
                    print!(
                        "TileMapEntry(0x{:02x}, 0x{:02x}, 0x{:02x})",
                        entry.0,
                        entry.1,
                        entry.2);
                }
                print!(",\n");
            }
            println!("];");
        }

        ToolMode::Palette => {
            let mut palettes = [Palette {entries: [PaletteEntry::new(); 4]}; 64];
            let mut current_palette: Palette = Palette {entries: [PaletteEntry::new(); 4]};

            let mut palette_idx:usize = 0;
            let mut palette_entry_idx:usize = 0;

            let mut _cy:usize = 2;
            let mut _cx:usize = 2;

            for _y in 0..16 {
                for _x in 0..16 {
                    if palette_entry_idx == 4 {
                        palettes[palette_idx] = current_palette;
                        palette_idx += 1;
                        palette_entry_idx = 0;
                    }

                    let pixels = canvas
                        .read_pixels(
                            Rect::new(_cx as i32, _cy as i32, 1, 1),
                            canvas.default_pixel_format())
                        .unwrap();
                    current_palette.entries[palette_entry_idx] = PaletteEntry {
                        r: pixels[0],
                        g: pixels[1],
                        b: pixels[2],
                        a: 0xff };
                    palette_entry_idx += 1;

                    _cx += 50;
                }

                _cx = 2;
                _cy += 38;
            }

            println!("lazy_static! {{");
            println!("\tstatic ref PAL_CNTL:Vec<Palette> = vec![");
            let mut palette_number = 0;
            for palette in palettes.iter() {
                println!("\t\t// #{}", palette_number);
                println!("\t\tPalette {{");
                println!("\t\t\tentries: [");
                for entry in palette.entries.iter() {
                    println!(
                        "\t\t\t\tPaletteEntry {{r: 0x{:02x}, g: 0x{:02x}, b: 0x{:02x}, a: 0xff}},",
                        entry.r,
                        entry.g,
                        entry.b);
                }
                println!("\t\t\t]");
                println!("\t\t}},");
                palette_number += 1;
            }
            println!("\t];");
            println!("}}");
        }

        ToolMode::Tile => {
            let mut _cy = 0;
            let mut _cx = 0;
            let mut match_palette: Palette = Palette {entries: [
                PaletteEntry { r: 0x00, g: 0x00, b: 0x00, a: 0xff},
                PaletteEntry { r: 0x94, g: 0x31, b: 0xec, a: 0xff},
                PaletteEntry { r: 0x06, g: 0x04, b: 0x8e, a: 0xff},
                PaletteEntry { r: 0xff, g: 0xf3, b: 0x14, a: 0xff},
            ]};

            let mut tiles = [[0 as u8; 8*8]; 256];
            let mut tile_idx = 0;

            for _ty in 0..2 {
                for _tx in 0..128 {
                    let mut tile_data = [0 as u8; 8*8];
                    for _y in 0..8 as usize {
                        for _x in 0..8 as usize {
                            let pixels = canvas
                                .read_pixels(
                                    Rect::new((_cx + _x) as i32, (_cy + _y) as i32, 1, 1),
                                    canvas.default_pixel_format())
                                .unwrap();
                            let mut entry_idx = 0;
                            for entry in match_palette.entries.iter() {
                                if entry.r == pixels[0]
                                && entry.g == pixels[1]
                                && entry.b == pixels[2] {
                                    tile_data[_y * 8 + _x] = entry_idx;
                                    break;
                                }
                                entry_idx += 1;
                            }
                        }
                    }
                    tiles[tile_idx] = tile_data;
                    tile_idx += 1;
                    _cx += 9;
                }
                _cx = 0;
                _cy += 9;
            }

            println!("static TILE_BITMAPS:[[u8; TILE_WIDTH*TILE_HEIGHT]; TILE_MAX] = [");
            let mut tile_number = 0;
            for tile_data in tiles.iter() {
                println!("\t// tile #{}", tile_number);
                print!("\t[");
                for _y in 0..8 as usize {
                    for _x in 0..8 as usize {
                        if _x > 0 {
                            print!(",");
                        }
                        print!("0x{:02x}", tile_data[_y * 8 + _x]);
                    }
                    if _y < 7 {
                        print!(",\n\t ");
                    }
                }
                println!("],\n");
                tile_number += 1;
            }
            println!("];");
        }

        ToolMode::Sprite => {
            let mut _cy = 0;
            let mut _cx = 0;
            let mut match_palette: Palette = Palette {entries: [
                PaletteEntry { r: 0x00, g: 0x00, b: 0x00, a: 0xff},
                PaletteEntry { r: 0xff, g: 0x9d, b: 0x9e, a: 0xff},
                PaletteEntry { r: 0xff, g: 0xf3, b: 0x14, a: 0xff},
                PaletteEntry { r: 0x0a, g: 0x07, b: 0xe8, a: 0xff},
            ]};

            let mut sprites = [[0 as u8; 16*16]; 128];
            let mut sprite_idx = 0;

            for _ty in 0..2 {
                for _tx in 0..68 {
                    if sprite_idx == 127 {
                        break;
                    }
                    let mut spr_data = [0 as u8; 16*16];
                    for _y in 0..16 as usize {
                        for _x in 0..16 as usize {
                            let pixels = canvas
                                .read_pixels(
                                    Rect::new((_cx + _x) as i32, (_cy + _y) as i32, 1, 1),
                                    canvas.default_pixel_format())
                                .unwrap();
                            let mut entry_idx = 0;
                            for entry in match_palette.entries.iter() {
                                if entry.r == pixels[0]
                                && entry.g == pixels[1]
                                && entry.b == pixels[2] {
                                    spr_data[_y * 16 + _x] = entry_idx;
                                    break;
                                }
                                entry_idx += 1;
                            }
                        }
                    }
                    sprites[sprite_idx] = spr_data;
                    sprite_idx += 1;
                    _cx += 17;
                }
                _cx = 0;
                _cy += 17;
            }

            println!("static SPRITE_BITMAPS:[[u8; SPRITE_WIDTH*SPRITE_HEIGHT]; SPRITE_MAX] = [");
            let mut sprite_number = 0;
            for spr_data in sprites.iter() {
                println!("\t// sprite #{}", sprite_number);
                print!("\t[");
                for _y in 0..16 as usize {
                    for _x in 0..16 as usize {
                        if _x > 0 {
                            print!(",");
                        }
                        print!("0x{:02x}", spr_data[_y * 16 + _x]);
                    }
                    if _y < 15 {
                        print!(",\n\t ");
                    }
                }
                println!("],\n");
                sprite_number += 1;
            }
            println!("];");
        }
    }

    'mainloop: loop {
        for event in sdl_context.event_pump().unwrap().poll_iter() {
            match event {
                Event::Quit{..} |
                Event::KeyDown {keycode: Option::Some(Keycode::Escape), ..} =>
                    break 'mainloop,
                _ => {}
            }
        }
    }
}

fn main() {
    let args: Vec<_> = env::args().collect();

    if args.len() < 3 {
        println!("Usage: cargo run pal|tile|sprite|map /path/to/image.png")
    } else {
        let mode = match args[1].as_ref() {
            "pal"     => ToolMode::Palette,
            "tile"    => ToolMode::Tile,
            "sprite"  => ToolMode::Sprite,
            "map"     => ToolMode::Map,
            _         => panic!("Unknown mode!")
        };

        run(mode, Path::new(&args[2]));
    }

//    run(ToolMode::Map, Path::new("assets/intro-tilemap.png"));
}

